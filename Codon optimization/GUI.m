% --- Executes on button press in start.
function start_Callback(hObject, eventdata, handles)
% hObject    handle to start (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
set(handles.OUTPUT, 'string', "");
pause(0.1)
text = ["Processing data. This may take a while", "Harmonizing your CDS", "Done, your harmonized CDS is saved as:"];
update_text(handles, text(1));
pause(0.1)
data_selection();
update_text(handles, text(2));
pause(0.1)
cross_species_harmonize(char(get(handles.CDS_input, 'string')), getGlobalRFC, getGlobalTypeIIs, get(handles.fileName, 'string'));
update_text(handles, [text(3); [char(get(handles.fileName, 'string')) '.txt'] ]);


function update_text(handles, text)
currString = convertCharsToStrings(get(handles.OUTPUT,'string'))
newString = [currString; text];
set(handles.OUTPUT, 'string', newString);

